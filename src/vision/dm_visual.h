// ============================================================
// 地圖枚舉
// ============================================================
enum class GameMap {
    UNKNOWN = 0,
    SHENGMEN = 1,   // 聖門
    SHANGDONG = 2,  // 商洞
    XUANYAN = 3,     // 玄巖
    FENGHUANG = 4,   // 鳳凰
};

// ============================================================
// NPC/怪物/物品圖檔
// ============================================================
namespace DMVisual {

    // ============================================================
    // 找圖結果
    // ============================================================
    struct FindResult {
        bool found = false;
        int x = -1;
        int y = -1;
        int width = 0;
        int height = 0;
    };

    // ============================================================
    // 初始化
    // ============================================================
    bool Init();

    // ============================================================
    // 找圖並返回座標（中心點）
    // ============================================================
    FindResult FindPic(const char* imageName, int left = 0, int top = 0,
                       int right = 1024, int bottom = 768, double sim = 0.8);

    // ============================================================
    // 點擊座標（使用 SendInput，DirectX 相容）
    // ============================================================
    void ClickAt(int x, int y);

    // ============================================================
    // 辨識地圖
    // ============================================================
    GameMap DetectMap();

    // ============================================================
    // 找怪物血條（用於鎖定）
    // ============================================================
    FindResult FindMonsterHP();

    // ============================================================
    // 找玩家血條
    // ============================================================
    FindResult FindPlayerHP();

    // ============================================================
    // 找箭矢
    // ============================================================
    FindResult FindArrow();

    // ============================================================
    // 找歸魂珠
    // ============================================================
    FindResult FindSoulPearl();

    // ============================================================
    // 找背包物品
    // ============================================================
    FindResult FindInventoryItem();

    // ============================================================
    // 找商店購買介面
    // ============================================================
    FindResult FindBuyArrow();
    FindResult FindBuyArrowSelect();
    FindResult FindBuyArrowYes();

    // ============================================================
    // 找 NPC（按城鎮）
    // ============================================================
    FindResult FindNPC_聖門();
    FindResult FindNPC_商洞();
    FindResult FindNPC_玄巖();
    FindResult FindNPC_鳳凰();

    // ============================================================
    // 通用 NPC 找圖（自動嘗試所有城鎮）
    // ============================================================
    FindResult FindNPC_Any();

    // ============================================================
    // 除錯測試
    // ============================================================
    bool TestImageFind();

    // ============================================================
    // 購買水/藥水介面
    // ============================================================
    FindResult FindBuyWater();
    FindResult FindBuyWaterConfirm();

    // ============================================================
    // 賣物
    // ============================================================
    FindResult FindSellItem();

    // ============================================================
    // 關閉商店（按 ESC x2，不影響其他狀態）
    // ============================================================
    void CloseShop();

    // ============================================================
    // 大漠狀態查詢（供 bot_logic.cpp 使用）
    // ============================================================
    bool IsInited();           // 是否已初始化
    bool IsActive();           // 是否處於活躍狀態
    void ResetDeadState();    // 重置死亡檢測狀態
    void ResetDeadTimer();     // 重置死亡計時器
    void UseStartCard();       // 使用歸魂符
    bool IsDeadDetected();    // 是否檢測到死亡

} // namespace DMVisual

