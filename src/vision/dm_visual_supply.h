// ============================================================
// dm_visual_supply.h
// 視覺補給模組（獨立模組，不影響現有狀態機）
//
// 使用 dm_visual.h 的圖檔找圖進行 NPC 對話和買賣
// 只在 TOWN_SUPPLY 狀態執行
// ============================================================

#pragma once
#ifndef WIN32_LEAN_AND_MEAN
#define WIN32_LEAN_AND_MEAN
#endif

#include <windows.h>
#include "../core/bot_logic.h"
#include "dm_visual.h"

// ============================================================
// 視覺補給命名空間
// ============================================================
namespace VisualSupply {

    // ============================================================
    // 供應階段枚舉
    // ============================================================
    enum class SupplyPhase {
        IDLE = 0,           // 空閒（未開始）
        FIND_NPC = 1,       // 找 NPC 並點擊
        WALK_TO_NPC = 2,    // 找不到 NPC，走到 NPC 附近
        WAIT_DIALOG = 3,    // 等待對話框
        OPEN_SHOP = 4,      // 點擊購買按鈕
        SELECT_ITEM_TAB = 5, // 選擇物品分頁（箭矢/消耗品）
        SELECT_ITEM = 6,     // 選擇物品
        CONFIRM_SELECT = 7,  // 確認選擇
        INPUT_QUANTITY = 8,  // 輸入數量
        CONFIRM_BUY = 9,    // 確認購買
        SELL_ITEMS = 10,    // 賣物品
        CONFIRM_SELL = 11,   // 確認賣
        CLOSE_SHOP = 12,    // 關閉商店
        DONE = 13           // 完成（可能沒實際補給）
    };

    // ============================================================
    // 供應配置
    // ============================================================
    struct SupplyConfig {
        int buyArrowQty = 300;       // 購買箭矢數量
        int buyHPQty = 300;          // 購買 HP 藥水數量
        int buyMPQty = 300;          // 購買 MP 藥水數量
        int buySPQty = 300;          // 購買 SP 藥水數量
        int maxRetries = 3;          // 每階段最大重試次數
        DWORD phaseTimeoutMs = 5000;  // 每階段超時（毫秒）
    };

    // ============================================================
    // 初始化
    // ============================================================
    void Init();

    // ============================================================
    // 開始供應（由狀態機在 TOWN_SUPPLY 時調用）
    // ============================================================
    void Begin();

    // ============================================================
    // 主迴圈（在 BotTick 的 TOWN_SUPPLY 狀態中調用）
    // 返回 true 表示供應流程已結束；用 IsSupplyCompleted() 區分成功/失敗
    // ============================================================
    bool Tick();

    // ============================================================
    // 重置供應狀態
    // ============================================================
    void Reset();

    // ============================================================
    // 獲取當前階段（調試用）
    // ============================================================
    SupplyPhase GetCurrentPhase();

    // ============================================================
    // 設置配置
    // ============================================================
    void SetConfig(const SupplyConfig& cfg);
    SupplyConfig GetConfig();

    // 獲取補給是否真正完成
    bool IsSupplyCompleted();

    // 獲取補給是否失敗（用於區分 FailSupply 和真正完成）
    bool IsSupplyFailed();

} // namespace VisualSupply
